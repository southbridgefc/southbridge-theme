/**
 * Add navigation menu to the footer, above return to top/credits.
 *
 * @since 1.0.0
 */
function sbridge_do_footer_social(){
	if ( ! has_nav_menu( 'footer' ) )
		return;

	$menu_args = array(
		'theme_location' => 'footer',
		'container_id'   => 'footer-nav',
		'depth'          => 1,
		'menu_class'     => genesis_get_option( 'nav_superfish' ) ? 'menu menu-footer superfish' : 'menu menu-footer',
		'walker'         => null,
	);
	wp_nav_menu( $menu_args );
}

/**
 * Add navigation menu to the footer, above return to top/credits.
 *
 * @since 1.0.0
 */
function sbridge_do_mini_menu(){
	if ( ! has_nav_menu( 'mini' ) )
