}

add_filter( 'wp_nav_menu_items', 'sbridge_nav_menu_items', 10, 2 );
function sbridge_nav_menu_items( $items, $args ) {
	if ( 'primary' == $args->theme_location ) {
		$form  = get_search_form( false );
		$items = str_replace( '<a href="#">Search</a>', $form . '<a href="#" onclick="return false;"><span class="search-glass"></span>Search</a>', $items );
	}
	return $items;
}

add_filter( 'wp_nav_menu_items', 'sbridge_nav_mini_rss_items', 10, 2 );
function sbridge_nav_mini_rss_items( $menu, $args ) {
	if ( 'mini' != $args->theme_location ) return $menu;
	
	$rss   = '<a rel="nofollow" href="' . get_bloginfo( 'rss2_url' ) . '">' . __( 'rss feed', 'sbridge' ) . '</a>';
	//$rss  .= '<a rel="nofollow" href="' . get_bloginfo( 'comments_rss2_url' ) . '">' . __( 'Comments', 'sbridge' ) . '</a>';
	$menu .= '<li class="rss"><span class="rss-icon"></span>' . $rss . '</li>';
	
	return $menu;
}

/**
 * Create HTML list of nav menu items.
 *
 * @package WordPress
 * @since 3.0.0
 * @uses Walker_Nav_Menu
 */
class SBridge_Walker_Nav_Menu extends Walker_Nav_Menu {

	/**
	 * @see Walker::start_el()
	 * @since 3.0.0
	 *
	 * @param string $output Passed by reference. Used to append additional content.
	 * @param object $item Menu item data object.
	 * @param int $depth Depth of menu item. Used for padding.
	 * @param int $current_page Menu item ID.
	 * @param object $args
	 */
	function start_el( &$output, $item, $depth = 0, $args = array(), $id = 0 ) {
		
		$indent = ( $depth ) ? str_repeat( "\t", $depth ) : '';

		$class_names = $value = '';

		$classes = empty( $item->classes ) ? array() : (array) $item->classes;
		$classes[] = 'menu-item-' . $item->ID;

		$class_names = join( ' ', apply_filters( 'nav_menu_css_class', array_filter( $classes ), $item, $args ) );
		$class_names = $class_names ? ' class="' . esc_attr( $class_names ) . '"' : '';

		$id = apply_filters( 'nav_menu_item_id', 'menu-item-'. $item->ID, $item, $args );
		$id = $id ? ' id="' . esc_attr( $id ) . '"' : '';

		$output .= $indent . '<li' . $id . $value . $class_names .'>';
		
		$icons = empty( $item->icon ) ? array() : (array) $item->icon;
		$icon_names = join( ' ', apply_filters( 'nav_menu_icons', array_filter( $icons ), $item, $args ) );
		
		$icon_names = $icon_names ? ' class="' . esc_attr( $icon_names ) . '"' : '';
		$output .= '<span' . $icon_names . '></span>';

		$attributes  = ! empty( $item->attr_title ) ? ' title="'  . esc_attr( $item->attr_title ) .'"' : '';
		$attributes .= ! empty( $item->target )     ? ' target="' . esc_attr( $item->target     ) .'"' : '';
		$attributes .= ! empty( $item->xfn )        ? ' rel="'    . esc_attr( $item->xfn        ) .'"' : '';
		$attributes .= ! empty( $item->url )        ? ' href="'   . esc_attr( $item->url        ) .'"' : '';

		$item_output = $args->before;
		$item_output .= '<a'. $attributes .'>';
		$item_output .= $args->link_before . apply_filters( 'the_title', $item->title, $item->ID ) . $args->link_after;
		$item_output .= '</a>';
		$item_output .= $args->after;

		$output .= apply_filters( 'walker_nav_menu_start_el', $item_output, $item, $depth, $args );
	}

}


add_action( 'wp_update_nav_menu_item', 'sbridge_nav_update', 10, 3 );
/*
 * Saves new field (icon field) to postmeta for navigation
 *
 * @param int   $menu_id         The ID of the menu. Required. If "0", makes the menu item a draft orphan.
 * @param int   $menu_item_db_id The ID of the menu item. If "0", creates a new menu item.
 * @param array $menu_item_data  The menu item's data.
 */
function sbridge_nav_update( $menu_id, $menu_item_db_id, $menu_item_data ) {
    if ( isset( $_REQUEST['menu-item-icon'] ) && is_array( $_REQUEST['menu-item-icon'] ) ) {
        $custom_value = $_REQUEST['menu-item-icon'][$menu_item_db_id];
        update_post_meta( $menu_item_db_id, '_menu_item_icon', $custom_value );
    }
}

add_filter( 'wp_setup_nav_menu_item', 'sbridge_setup_nav_item' );
/*
 * Adds value of new field to $item object that will be passed to Walker_Nav_Menu_Edit_Custom
 *
 * @param  WP_Post_Object $menu_item Menu item post object.
 * @return WP_Post_Object $menu_item Modified Menu item post object.
 */
function sbridge_setup_nav_item( $menu_item ) {
    $menu_item->icon = get_post_meta( $menu_item->ID, '_menu_item_icon', true );
    return $menu_item;
}

